                <h3>purpose</h3>
                <p>
A large portfolio exists of material, content and experiences that want to be displayed publicly without a network to accommodate the multitude of mediums and subjects that I have taken part in. This website is the result of that and even it, though it displays a lot of photography, illustrations, literature and audio; still does not cover all areas. Apps have been written, 3D models have been made, animations keyframed, videos edited and yet there is more content that just aches to be uploaded for everyone to view.
<br><br>
Though the website is semi-successful at portraying my vast array of expertise, it is however, a beautiful and interactive way to view the content I have created in a concise and exclusive package with options to contact me and getting to know me better through viewing my content and their descriptions. 
                </p>
                <h3>design</h3>
                <p>
<em>“Clean. Unobtrusive. Logical. Unique. Timeless. Modern. Adaptive. Multifaceted.”</em>
<br><br>
— These are all words used by others, friends, acquaintances and strangers alike to (in one word) describe what vibe is given off the design of this webpage.
<br><br>
On a foundational level, this website features a design scheme that aims to merge very different mediums together in a clean, consistent and simply pleasant mean. The goal was to emphasise continuity, making all pages feel like they are part of the same unit - without detracting from their own individual identities and purposes. Through an overall grayscale design; vibrant colours emphasise importance and individuality. Through clean, soft animations; the user is guided about in a seamless and undocumented manner, exploring as they go along in their own pace. 
<br><br>
Design is much more than pure aesthetics. Good design is logical and yet daring. Good design is unobtrusive and yet inviting. Good design is, on a fundamental level, all-inclusive. It’s unfortunately all too easy to think very two-dimensionally about design. See, it is more than just a well-dressed person - it is a welcoming individual that, alongside an appealing charisma, vast knowledge and wholesome values makes you feel special.
<br><br>
This website does its very best to allow any user to (without hinderance and regardless of circumstances) navigate the website in an easy and intuitive manner. It is unfortunately all too easy as a designer to get lost in the “make-up” when getting a website ready for the world to behold. I personally take great pride in this list of “extra” attention to detail to increase inclusivity:
                </p>
                <ul>
<li>All important navigation items are tabbable, allowing keyboard navigation.</li>

<li>A fully supported <a href="#" onclick="$('#header-nightmode-button-wrapper').click(); return false;">Nightmode</a> is available to us night owls.</li>

<li>Portable touch devices are catered to just as much as desktops / laptops.</li>

<li>Some images are smart and increase / decrease in quality to improve performance in certain situations.</li>

<li>Though admittedly not as stunning, users that surf anonymously still have all the core content available to them and features that don’t work are hidden instead of broken.</li>

<li>Writing a longer email to me using my <a href="contact.php">contact</a> form in a hurry? Do not fret, you can minimise and even navigate to another page and resume your message at a later point.</li>

<li>To maximise reading comfort, literature that you read is in a more comfortable font with options to make the text bigger and change the colour of the background to your preferred reading habits.</li>

<li>Clicking or tapping the header moves you to the top of the page. Click or tap again to open the dropdown menu.</li>
                    
<li>The latest versions of <strong>Chrome</strong>, <strong>Edge</strong>, <strong>Firefox</strong>, <strong>Opera</strong> and <strong>Safari</strong> are actively supported on both Windows and macOS. All major browsers on iOS are also actively supported.</li>

                </ul>

